package latihan;
class LatihanArray{
public boolean LatihanArray(int n) {
        if (n % 3 != 0){
            return false;
        } if (n == 0) {
            return false;
        } else {
            return true;
        }
    }
    public static void main (String[] args){
        System.out.println(27);
        System.out.println(0);
        System.out.println(-1);
        System.out.println(45);
    }
}